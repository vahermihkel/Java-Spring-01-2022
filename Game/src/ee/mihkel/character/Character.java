package ee.mihkel.character;

// encapsulation - kapseldus
public class Character {
    protected int xCoord;
    protected int yCoord;
    protected char symbol;

    public int getxCoord() {
        return xCoord;
    }

    public int getyCoord() {
        return yCoord;
    }

    public char getSymbol() {
        return symbol;
    }
}
